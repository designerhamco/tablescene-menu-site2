export function getUiText(language, key) {
  return uiText[language]?.[key] || uiText.ko[key] || key;
}

// 데이터 문구가 { ko, en, zh, ja } 형태일 때 현재 언어 값을 가져옵니다.
// 선택한 언어 값이 비어 있으면 ko 값을 대신 보여줍니다.
export function getLocalizedText(value, language) {
  if (value === null || value === undefined) return "";
  if (typeof value === "string" || typeof value === "number") return String(value);
  if (typeof value !== "object") return "";

  return value[language] || value.ko || "";
}
